// filters
export const productFiltersController = async (req, res) => {
  try {
    console.log("Request Body:", req.body); // Log the request body
    const { checked = [], radio = [] } = req.body; // Provide default values
    let args = {};
    if (checked.length > 0) args.category = { $in: checked }; // Use $in for array matching
    if (radio.length) args.price = { $gte: radio[0], $lte: radio[1] };
    console.log("Filter Args:", args); // Log the filter arguments
    const product = await Product.find(args);
    return res.status(200).send({
      message: "Product filtered successfully",
      success: true,
      products: product, // Change the key to 'products' to match client-side expectation
    });
  } catch (error) {
    console.log("Error:", error);
    res.status(400).send({
      message: "Error in product filter controller",
      success: false,
      error: error.message, // Include error message for better debugging
    });
  }
};

